function assertSameShape(a, b, path = '') {
  if (a === null || b === null) throw new Error(`null at ${path}`);
  const ta = Array.isArray(a) ? 'array' : typeof a;
  const tb = Array.isArray(b) ? 'array' : typeof b;
  if (ta !== tb) throw new Error(`type mismatch at ${path}: ${ta} vs ${tb}`);
  if (ta === 'object' && !Array.isArray(a)) {
    const ak = Object.keys(a).sort();
    const bk = Object.keys(b).sort();
    if (ak.join(',') !== bk.join(',')) {
      const missing = ak.filter(k => !bk.includes(k));
      const extra = bk.filter(k => !ak.includes(k));
      throw new Error(`keys differ at ${path}. missing in ru: ${missing.join(',')} extra in ru: ${extra.join(',')}`);
    }
    for (const k of ak) assertSameShape(a[k], b[k], `${path}.${k}`);
  } else if (ta === 'array') {
    if (a.length !== b.length) throw new Error(`array length at ${path}`);
    for (let i = 0; i < a.length; i++) assertSameShape(a[i], b[i], `${path}[${i}]`);
  }
}

